# Після скидання середовища потрібно повторно імпортувати бібліотеки та обчислити кут

import numpy as np
# Координати першої лінії
x1, y1 = 243.0, 152.0
x2, y2 = 231.0, 151.5

# Координати другої лінії
x3, y3 = 231.0, 151.0
x4, y4 = 66.0, 156.5

# Обчислення нахилів прямих
m1 = (y2 - y1) / (x2 - x1)
m2 = (y4 - y3) / (x4 - x3)

# Обчислення кута між прямими
theta_rad = np.arctan(abs((m1 - m2) / (1 + m1 * m2)))

# Перетворення у градуси
theta_deg = np.degrees(theta_rad)
theta_deg
print(theta_deg)
print(theta_rad)
